<?php

$var1 = 2;
$var2 = 10;
$var3 = 4;

// SUMA
echo "$var1 + $var3 = ".$var1+$var3, "<br/>";

// RESTA
echo "$var2 - $var3 = ".$var2-$var3, "<br/>";

//  DIVISIO
echo "$var2 / $var1 = ".$var2/$var1, "<br/>";

// MODUL
echo "$var3 % $var1 = ".$var3%$var1, "<br/>";

// MULTIPLICACIO
echo "$var2 * $var3 = ".$var2*$var3, "<br/>";